//字串用法
let phrase = "hello,Mr.White.";
//            01234567890123

document.write(phrase + "<br>");
document.write(phrase.length + "<br>");  //字串長度
document.write(phrase.toUpperCase() + "<br>");   //大寫
document.write(phrase.toLowerCase() + "<br>");   //小寫
document.write(phrase.charAt(1) + "<br>");   //回傳第()位的數字
document.write(phrase.indexOf("M") + "<br>");    //搜尋()的字為第幾位
document.write(phrase.substring(1,10) + "<br>"); //回傳第幾位到第幾位的字串