//數字用法
let num1 = 6;
document.write(3+2 + "<br>");
document.write(num1 + "<br>");
document.write(Math.abs(num1) + "<br>");    //取絕對值
document.write(Math.max(2, 5, 7, 10, 99, 50, num1) + "<br>");   //回傳最大的
document.write(Math.min(2, 5, 7, 10, 99, 50, num1) + "<br>");   //回傳最小的
document.write(Math.round(num1) + "<br>");  //四捨五入再回傳
document.write(Math.pow(2, 4) + "<br>");    //範例：2的四次方
document.write(Math.sqrt(4, 2) + "<br>");   //範例：4開耕號
document.write(Math.random() + "<br>"); //0~1隨機數
document.write(Math.round(Math.random() * 10) + "<br>");    //搭配四捨五入做出0~10的隨機數
